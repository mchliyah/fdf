#include "fdf.h"

void	init(t_fdf *fdf)
{
	if (fdf->i == 0)
	{
		fdf->x0 = 800;
		fdf->y0 = 0;
		fdf->x = 800;
		fdf->y = 0;
	}
	else
	{
		fdf->y0 = fdf->y0 + 11;
		fdf->x0 = 800;
		fdf->x = 800;
	}
}
